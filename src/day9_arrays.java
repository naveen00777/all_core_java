public class day9_arrays {
    public static void main(String[] args) {
        int a[] = {1, 2, 4, 5, 6, 7, 8};
        char b[] = {'h', 'e', 'l', 'l', 'o'};
        for (int i = 0; i < a.length; i++) {
            System.out.print(a[i]);
        }
        for (int j = 0; j <= b.length; j++) {
            System.out.print(b[j]);
        }
    }
}